public class Exercise09 {

    public static void main(String[] args) {
        // 1. Write a loop to sum all numbers between 7 and 16. //Answer 115
        // 2. Print the result.
        int sum = 0;
        for (int n = 7; n <= 16; n++)
            sum += n;
        System.out.println(sum);
    }
}
